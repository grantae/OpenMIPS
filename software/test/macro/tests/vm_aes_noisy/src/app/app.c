#include "aes.h"
#include "kernel.h"
#include <string.h>

static uint8 key[16] = {0x9d, 0xc2, 0xc8, 0x4a, 0x37, 0x85, 0x0c, 0x11, 0x69, 0x98, 0x18, 0x60, 0x5f, 0x47, 0x95, 0x8c};
static uint8 iv[16] = {0x25, 0x69, 0x53, 0xb2, 0xfe, 0xab, 0x2a, 0x04, 0xae, 0x01, 0x80, 0xd8, 0x33, 0x5b, 0xbe, 0xd6};
static uint8 ptxt[16] = {0x2e, 0x58, 0x66, 0x92, 0xe6, 0x47, 0xf5, 0x02, 0x8e, 0xc6, 0xfa, 0x47, 0xa5, 0x5a, 0x2a, 0xab};
static uint8 ctxt[16] = {0x64, 0xa3, 0x41, 0x73, 0x6f, 0x8b, 0x8c, 0x67, 0x0e, 0xed, 0x4c, 0x7a, 0x4b, 0x5d, 0xca, 0x3f};

int main(void) {
  set_timer_cycles(2000);
  enable_int(INT_TIMER);

  uint8 buf_1[16];
  uint8 buf_2[16];
  uint8 buf_3[16];
  memcpy(buf_1, iv, 16);

  aes_128_encrypt(ptxt, 16, buf_2, iv, key);
  if (memcmp(ctxt, buf_2, 16) != 0) {
    return 0;
  }
  aes_128_decrypt(buf_2, 16, buf_3, buf_1, key);
  if (memcmp(buf_3, ptxt, 16) != 0) {
    return 0;
  }
  disable_int(INT_TIMER);
  set_scratch(get_timer_bells());
  return 1;
}

